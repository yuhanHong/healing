package com.healing.product.dto;

public class ProductDetailDto {
	private int product_detail_number;
	private int product_day_number;
	private int product_number;
	private String product_detail_name;
	private String product_detail_explain;

	public int getProduct_detail_number() {
		return product_detail_number;
	}
	public void setProduct_detail_number(int product_detail_number) {
		this.product_detail_number = product_detail_number;
	}
	public int getProduct_day_number() {
		return product_day_number;
	}
	public void setProduct_day_number(int product_day_number) {
		this.product_day_number = product_day_number;
	}
	public int getProduct_number() {
		return product_number;
	}
	public void setProduct_number(int product_number) {
		this.product_number = product_number;
	}
	public String getProduct_detail_name() {
		return product_detail_name;
	}
	public void setProduct_detail_name(String product_detail_name) {
		this.product_detail_name = product_detail_name;
	}
	public String getProduct_detail_explain() {
		return product_detail_explain;
	}
	public void setProduct_detail_explain(String product_detail_explain) {
		this.product_detail_explain = product_detail_explain;
	}
}
