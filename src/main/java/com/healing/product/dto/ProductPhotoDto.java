package com.healing.product.dto;

public class ProductPhotoDto {
	private int product_number;
	private int product_photo_number;
	private int product_photo_size;
	private int product_photo_path;
	private int product_photo_filename;
	public int getProduct_number() {
		return product_number;
	}
	public void setProduct_number(int product_number) {
		this.product_number = product_number;
	}
	public int getProduct_photo_number() {
		return product_photo_number;
	}
	public void setProduct_photo_number(int product_photo_number) {
		this.product_photo_number = product_photo_number;
	}
	public int getProduct_photo_size() {
		return product_photo_size;
	}
	public void setProduct_photo_size(int product_photo_size) {
		this.product_photo_size = product_photo_size;
	}
	public int getProduct_photo_path() {
		return product_photo_path;
	}
	public void setProduct_photo_path(int product_photo_path) {
		this.product_photo_path = product_photo_path;
	}
	public int getProduct_photo_filename() {
		return product_photo_filename;
	}
	public void setProduct_photo_filename(int product_photo_filename) {
		this.product_photo_filename = product_photo_filename;
	}
}
